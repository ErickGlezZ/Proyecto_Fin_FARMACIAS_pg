    private void btnEditarPacientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarPacientesActionPerformed

        Paciente p = new Paciente(ssnPaciente,
            cajaNombreCambios.getText(),
            cajaApPaternoCambios.getText(),
            cajaApMaternoCambios.getText(),
            Byte.parseByte(spEdadCambios.getValue().toString()),
            cbSSNMedicoCabCambios.getSelectedItem().toString(),
            cajaCalleCambios.getText(),
            cajaNumeroCambios.getText(),
            cajaColoniaCambios.getText(),
            Integer.parseInt(cajaCodPostalCambios.getText()));

        if (controller.editar(p)) {
            JOptionPane.showMessageDialog(this,"Registro Editado CORRECTAMENTE");
            System.out.println("Registro Editado CORRECTAMENTE");

            limpiarCampos();
            dispose();
        }else{
            JOptionPane.showMessageDialog(this,"Error en la insercion");
            System.out.println("ERROR en la insercion");
        }
    }//GEN-LAST:event_btnEditarPacientesActionPerformed

