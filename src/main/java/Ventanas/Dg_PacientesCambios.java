    private void cargarMedicosEnCombo(){
        cbSSNMedicoCabCambios.removeAllItems();
        
        cbSSNMedicoCabCambios.addItem("Elije Médico...");
        ResultSet rs = controller.obtenerMedicos();
        try {
            while (rs.next()) {
                String ssn = rs.getString("SSN");

                cbSSNMedicoCabCambios.addItem(ssn);

            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar médicos");
        }
        
    }
    
    private void actualizarLabelMedico(){
        
        if (cbSSNMedicoCabCambios.getSelectedIndex() == 0) return; 
        String ssn = cbSSNMedicoCabCambios.getSelectedItem().toString();
        String nombre = controller.obtenerNombreCompleto(ssn);
        lblNombreMedico.setText("Médico asig. " + nombre);
    }
    

    private void cargarDatosPaciente() {

        ResultSet rs = controller.obtenerPacientePorSSN(ssnPaciente);

        try {

            if (rs.next()) {
                
                cajaNombreCambios.setText(rs.getString("nombre"));
                
                cajaApPaternoCambios.setText(rs.getString("ape_paterno"));
                
                cajaApMaternoCambios.setText(rs.getString("ape_materno"));
                
                spEdadCambios.setValue(rs.getInt("edad"));

                cbSSNMedicoCabCambios.setSelectedItem(rs.getString("ssn_medico_cabecera"));

                cajaCalleCambios.setText(rs.getString("calle"));
                
                cajaNumeroCambios.setText(rs.getString("numero"));

                cajaColoniaCambios.setText(rs.getString("colonia"));

                cajaCodPostalCambios.setText(rs.getString("codigo_postal"));

                actualizarLabelMedico();
               
            }

        } catch (SQLException e) {

            e.printStackTrace();

            JOptionPane.showMessageDialog(
                    this,
                    "Error al cargar datos"
            );
        }
    }

